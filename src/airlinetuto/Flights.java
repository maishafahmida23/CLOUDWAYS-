    private void saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMouseClicked
        // TODO add your handling code here:

        if (flightcode.getText().isEmpty() || numberofseats.getText().isEmpty() || source.getSelectedIndex() == -1 || destination.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Missing Information");
        } else {
            try {

                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedb", "root", "");
                PreparedStatement Add = Con.prepareStatement("insert into FlightTbl values(?,?,?,?,?)");
                Add.setString(1, flightcode.getText());
                Add.setString(2, source.getSelectedItem().toString());
                Add.setString(3, destination.getSelectedItem().toString());
                Add.setString(4, takeofdate.getDate().toString());
                Add.setInt(5, Integer.valueOf(numberofseats.getText()));

                int row = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Flight Added");
                Con.close();
                DisplayFlight();
                Clear();

            } catch (Exception e) {

                JOptionPane.showMessageDialog(this, e);

            }
        }
    }//GEN-LAST:event_saveMouseClicked

