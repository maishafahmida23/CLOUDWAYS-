    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked

        if (Key == "") {
            JOptionPane.showMessageDialog(this, "Select A Passenger");
        } else {
            try {
                //CountPassengers();
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedb", "root", "");
                String Query = "Update FlightTbl set FISource=?,FIDest=?,FIDate=?,FISeats=? where FICode=?";
                PreparedStatement Add = Con.prepareStatement(Query);
                Add.setString(5, Key);
                Add.setString(1, source.getSelectedItem().toString());
                Add.setString(2, destination.getSelectedItem().toString());
                Add.setString(3, takeofdate.getDate().toString());
                Add.setString(4, numberofseats.getText());
                int row = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Flight Updated");
                Con.close();
                DisplayFlight();
                Clear();

            } catch (Exception e) {

                JOptionPane.showMessageDialog(this, e);

            }
        }
    }//GEN-LAST:event_editMouseClicked

